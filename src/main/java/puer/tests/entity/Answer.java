package puer.tests.entity;

public class Answer {
    private int id;
    private String name;
    private int value;

    public Answer(String name) {
        this.name = name;
    }

    public Answer(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
